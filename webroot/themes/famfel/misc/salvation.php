<h3>1 JOHN 4:1-21</h3>
<ol>
<li>Dear friends, do not believe every spirit, but test the spirits to see whether they are from God, because many false prophets have gone out into the world.</li>
<li>This is how you can recognize the Spirit of God: Every spirit that acknowledges that Jesus Christ has come in the flesh is from God,</li>
<li>but every spirit that does not acknowledge Jesus is not from God. This is the spirit of the antichrist, which you have heard is coming and even now is already in the world.</li>
<li>You, dear children, are from God and have overcome them, because the one who is in you is greater than the one who is in the world.</li>
<li>They are from the world and therefore speak from the viewpoint of the world, and the world listens to them.</li>
<li>We are from God, and whoever knows God listens to us; but whoever is not from God does not listen to us. This is how we recognize the Spirit of truth and the spirit of falsehood.</li>
<li>Dear friends, let us love one another, for love comes from God. Everyone who loves has been born of God and knows God.</li>
<li>Whoever does not love does not know God, because God is love.</li>
<li>This is how God showed his love among us: He sent his one and only Son into the world that we might live through him.</li>
<li>This is love: not that we loved God, but that he loved us and sent his Son as an atoning sacrifice for our sins.</li>
<li>Dear friends, since God so loved us, we also ought to love one another.</li>
<li>No one has ever seen God; but if we love one another, God lives in us and his love is made complete in us.</li>
<li>We know that we live in him and he in us, because he has given us of his Spirit.</li>
<li>And we have seen and testify that the Father has sent his Son to be the Savior of the world.</li>
<li>If anyone acknowledges that Jesus is the Son of God, God lives in him and he in God.</li>
<li>And so we know and rely on the love God has for us. God is love. Whoever lives in love lives in God, and God in him.</li>
<li>In this way, love is made complete among us so that we will have confidence on the day of judgment, because in this world we are like him.</li>
<li>There is no fear in love. But perfect love drives out fear, because fear has to do with punishment. The one who fears is not made perfect in love.</li>
<li>We love because he first loved us.</li>
<li>If anyone says, "I love God," yet hates his brother, he is a liar. For anyone who does not love his brother, whom he has seen, cannot love God, whom he has not seen.</li>
<li>And he has given us this command: Whoever loves God must also love his brother. </li>
</ol>

